# INPUT = armazena entrada de dados do usuário

nome = input("Digite seu nome:")
idade = int(input("Digite sua idade:")) #Convertido para int
peso = float(input("Digite o seu peso:")) #Convertido para float

# Função type(), vai mostrar o tipo de var de cada uma
# nome = str | idade = int | peso = float
print(nome)
print(type(idade))
print(type(peso))